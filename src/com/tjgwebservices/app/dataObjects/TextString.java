package com.tjgwebservices.app.dataObjects;

public class TextString {
	private int textID;
	private String text;

	public TextString(){
	}

	public int getTextID() {
		return textID;
	}

	public void setTextID(int textID) {
		this.textID = textID;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	
}
